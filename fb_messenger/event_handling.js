function receivedMessage(event, sessionIDs) {

    var senderID = event.sender.id;
    var recipientID = event.recipient.id;
    var timeOfMessage = event.timestamp;
    var message = event.message;

    if (!sessionIDs.has(senderID)) {
        sessionIDs.set(senderID, uuid.v1());
    }                
    var sessionID = sessionIDs.get(senderID);

    console.log("Received message for user %d and page %d at %d with message:", senderID, recipientID, timeOfMessage);
    console.log(JSON.stringify(message));

    var isEcho = message.is_echo;
    var messageId = message.mid;
    var appId = message.app_id;
    var metadata = message.metadata;

    // You may get a text or attachment but not both
    var messageText = message.text;
    var messageAttachments = message.attachments;
    var quickReply = message.quick_reply;

    if (isEcho) {
        handleEcho(messageId, appId, metadata);
        return;
    } else if (quickReply) {
        handleQuickReply(senderID, sessionID, quickReply, messageId);
        return;
    }


    if (messageText) {
        //send message to api.ai
        apiai_interface.sendToApiAi(senderID, sessionID, messageText);
    } else if (messageAttachments) {
        handleMessageAttachments(senderID, sessionID, messageAttachments);
    }
}


function handleMessageAttachments(senderID, sessionID, messageAttachments){
    // attachments is an array
    for (let a of messageAttachments){
        switch (a.type){
            case "location":
                fb_attach.handleLocation(senderID, sessionID, a);
                break;
        }
    }
}

function handleQuickReply(senderID, sessionID, quickReply, messageId) {
    var quickReplyPayload = quickReply.payload;
    console.log("Quick reply for message %s with payload %s", messageId, quickReplyPayload);
    //send payload to api.ai
    sendToApiAi(senderID, sessionID, quickReplyPayload);
}

